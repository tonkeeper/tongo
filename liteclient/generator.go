//go:build ignore

package main

import (
	"fmt"
	"io/ioutil"
	"os"

	"github.com/tonkeeper/tongo/tl/parser"
)

func main() {
	scheme, err := ioutil.ReadFile("lite_api.tl")
	if err != nil {
		panic(err)
	}
	parsed, err := parser.Parse(string(scheme))
	if err != nil {
		panic(err)
	}

	g := parser.NewGenerator(nil, "*Client")

	types, err := g.LoadTypes(parsed.Declarations)
	if err != nil {
		panic(err)
	}
	functions, err := g.LoadFunctions(parsed.Functions)
	if err != nil {
		panic(err)
	}

	f, err := os.Create("generated.go")
	if err != nil {
		panic(err)
	}
	_, err = fmt.Fprint(f, `package liteclient
// Code autogenerated. DO NOT EDIT. 

import (
	"bytes"
	"context"
	"encoding/binary"
	"fmt"
	"github.com/tonkeeper/tongo/tl"
	"io"
)
`)
	if err != nil {
		panic(err)
	}
	_, err = f.WriteString(types)
	if err != nil {
		panic(err)
	}
	_, err = f.WriteString(functions)
	if err != nil {
		panic(err)
	}
}
